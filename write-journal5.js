const fs = require('fs');
const lines = [];
lines.push("'use client'");
lines.push("");
lines.push("import { useState, useEffect } from 'react'");
lines.push("import { createClient } from '../../lib/supabase'");
lines.push("import { LineChart, Line, XAxis, YAxis, Tooltip, ResponsiveContainer } from 'recharts'");
lines.push("");
lines.push("type JournalEntry = { id: string; date: string; mood: number; energy: number; sleep: number; notes: string }");
lines.push("");
lines.push("function ProgressRing({ value, max, color, label, size = 70 }: { value: number; max: number; color: string; label: string; size?: number }) {");
lines.push("  const radius = (size - 10) / 2");
lines.push("  const circumference = 2 * Math.PI * radius");
lines.push("  const progress = Math.min(value / max, 1)");
lines.push("  const strokeDashoffset = circumference * (1 - progress)");
lines.push("  return (");
lines.push("    <div style={{display:'flex',flexDirection:'column',alignItems:'center',gap:'4px'}}>");
lines.push("      <svg width={size} height={size} style={{transform:'rotate(-90deg)'}}>"); 
lines.push("        <circle cx={size/2} cy={size/2} r={radius} fill='none' stroke='#1a1a1a' strokeWidth={6} />");
lines.push("        <circle cx={size/2} cy={size/2} r={radius} fill='none' stroke={color} strokeWidth={6} strokeDasharray={circumference} strokeDashoffset={strokeDashoffset} strokeLinecap='round' style={{transition:'stroke-dashoffset 0.5s ease'}} />");
lines.push("        <text x={size/2} y={size/2} textAnchor='middle' dominantBaseline='middle' fill='white' fontSize={size*0.22} fontWeight='700' style={{transform:`rotate(90deg)`,transformOrigin:`${size/2}px ${size/2}px`}}>{value > 0 ? value.toFixed(1) : '—'}</text>");
lines.push("      </svg>");
lines.push("      <span style={{fontSize:'10px',color:'#4dbd4d',textAlign:'center'}}>{label}</span>");
lines.push("    </div>");
lines.push("  )");
lines.push("}");
lines.push("");
lines.push("export default function JournalPage() {");
lines.push("  const [entries, setEntries] = useState<JournalEntry[]>([])");
lines.push("  const [loading, setLoading] = useState(true)");
lines.push("  const [showForm, setShowForm] = useState(false)");
lines.push("  const [editingId, setEditingId] = useState<string | null>(null)");
lines.push("  const [mood, setMood] = useState(3)");
lines.push("  const [energy, setEnergy] = useState(3)");
lines.push("  const [sleep, setSleep] = useState('')");
lines.push("  const [notes, setNotes] = useState('')");
lines.push("  const [saving, setSaving] = useState(false)");
lines.push("  const [error, setError] = useState('')");
lines.push("  const [showChart, setShowChart] = useState(false)");
lines.push("  const [chartRange, setChartRange] = useState<7|30|'all'>(7)");
lines.push("  const g = '#39ff14'");
lines.push("  const dg = '#4dbd4d'");
lines.push("  const mg = '#2d5a2d'");
lines.push("  const cb = '#0d0d0d'");
lines.push("  const bd = '#1a1a1a'");
lines.push("");
lines.push("  useEffect(() => { loadEntries() }, [])");
lines.push("");
lines.push("  async function loadEntries() {");
lines.push("    setLoading(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setLoading(false); return }");
lines.push("    const { data } = await supabase.from('journal_entries').select('*').order('date', { ascending: true })");
lines.push("    setEntries(data || [])");
lines.push("    setLoading(false)");
lines.push("  }");
lines.push("");
lines.push("  function startNew() { setEditingId(null); setMood(3); setEnergy(3); setSleep(''); setNotes(''); setShowForm(true); setError('') }");
lines.push("  function startEdit(entry: JournalEntry) { setEditingId(entry.id); setMood(entry.mood); setEnergy(entry.energy); setSleep(String(entry.sleep)); setNotes(entry.notes || ''); setShowForm(true); setError('') }");
lines.push("");
lines.push("  async function saveEntry() {");
lines.push("    setError('')");
lines.push("    if (!sleep || isNaN(parseFloat(sleep))) { setError('Please enter how many hours you slept.'); return }");
lines.push("    setSaving(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setError('You must be signed in.'); setSaving(false); return }");
lines.push("    if (editingId) {");
lines.push("      await supabase.from('journal_entries').update({ mood, energy, sleep: parseFloat(sleep), notes: notes.trim() }).eq('id', editingId)");
lines.push("    } else {");
lines.push("      const today = new Date().toISOString().split('T')[0]");
lines.push("      await supabase.from('journal_entries').insert({ user_id: user.id, date: today, mood, energy, sleep: parseFloat(sleep), notes: notes.trim() })");
lines.push("    }");
lines.push("    setShowForm(false); setEditingId(null); setSaving(false)");
lines.push("    loadEntries()");
lines.push("  }");
lines.push("");
lines.push("  async function deleteEntry(id: string) {");
lines.push("    const supabase = createClient()");
lines.push("    await supabase.from('journal_entries').delete().eq('id', id)");
lines.push("    loadEntries()");
lines.push("  }");
lines.push("");
lines.push("  function formatDate(dateStr: string) {");
lines.push("    return new Date(dateStr + 'T12:00:00').toLocaleDateString('en-US', { month: 'short', day: 'numeric' })");
lines.push("  }");
lines.push("");
lines.push("  function moodLabel(val: number) {");
lines.push("    return ({1:'Rough',2:'Low',3:'Okay',4:'Good',5:'Great'} as Record<number,string>)[val] || val.toString()");
lines.push("  }");
lines.push("");
lines.push("  // --- STATS ---");
lines.push("  function getStreak() {");
lines.push("    if (entries.length === 0) return 0");
lines.push("    const dates = new Set(entries.map(e => e.date))");
lines.push("    let streak = 0");
lines.push("    const today = new Date()");
lines.push("    for (let i = 0; i < 365; i++) {");
lines.push("      const d = new Date(today)");
lines.push("      d.setDate(d.getDate() - i)");
lines.push("      const dateStr = d.toISOString().split('T')[0]");
lines.push("      if (dates.has(dateStr)) { streak++ } else { break }");
lines.push("    }");
lines.push("    return streak");
lines.push("  }");
lines.push("");
lines.push("  function getWeekAvg(weeksAgo = 0) {");
lines.push("    const now = new Date()");
lines.push("    const start = new Date(now)");
lines.push("    start.setDate(start.getDate() - (7 * weeksAgo) - 6)");
lines.push("    const end = new Date(now)");
lines.push("    end.setDate(end.getDate() - (7 * weeksAgo))");
lines.push("    const startStr = start.toISOString().split('T')[0]");
lines.push("    const endStr = end.toISOString().split('T')[0]");
lines.push("    const weekEntries = entries.filter(e => e.date >= startStr && e.date <= endStr)");
lines.push("    if (weekEntries.length === 0) return null");
lines.push("    return {");
lines.push("      mood: weekEntries.reduce((s, e) => s + e.mood, 0) / weekEntries.length,");
lines.push("      energy: weekEntries.reduce((s, e) => s + e.energy, 0) / weekEntries.length,");
lines.push("      sleep: weekEntries.reduce((s, e) => s + e.sleep, 0) / weekEntries.length,");
lines.push("      days: weekEntries.length,");
lines.push("    }");
lines.push("  }");
lines.push("");
lines.push("  function getDaysLoggedThisWeek() {");
lines.push("    const now = new Date()");
lines.push("    const start = new Date(now)");
lines.push("    start.setDate(start.getDate() - 6)");
lines.push("    const startStr = start.toISOString().split('T')[0]");
lines.push("    const endStr = now.toISOString().split('T')[0]");
lines.push("    return entries.filter(e => e.date >= startStr && e.date <= endStr).length");
lines.push("  }");
lines.push("");
lines.push("  function getChartData() {");
lines.push("    const sorted = [...entries].sort((a, b) => a.date.localeCompare(b.date))");
lines.push("    if (chartRange === 'all') return sorted.map(e => ({ date: formatDate(e.date), mood: e.mood, energy: e.energy, sleep: e.sleep }))");
lines.push("    const now = new Date()");
lines.push("    const cutoff = new Date(now)");
lines.push("    cutoff.setDate(cutoff.getDate() - (chartRange - 1))");
lines.push("    const cutoffStr = cutoff.toISOString().split('T')[0]");
lines.push("    return sorted.filter(e => e.date >= cutoffStr).map(e => ({ date: formatDate(e.date), mood: e.mood, energy: e.energy, sleep: e.sleep }))");
lines.push("  }");
lines.push("");
lines.push("  const streak = getStreak()");
lines.push("  const thisWeek = getWeekAvg(0)");
lines.push("  const lastWeek = getWeekAvg(1)");
lines.push("  const daysThisWeek = getDaysLoggedThisWeek()");
lines.push("  const chartData = getChartData()");
lines.push("  const sortedEntries = [...entries].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())");
lines.push("  const tooltipStyle = {contentStyle:{background:cb,border:'1px solid '+bd,borderRadius:'6px',fontSize:'12px'}}");
lines.push("");
lines.push("  function Delta({ current, previous, label }: { current: number; previous: number; label: string }) {");
lines.push("    const diff = current - previous");
lines.push("    const color = diff > 0 ? g : diff < 0 ? '#ff4444' : dg");
lines.push("    const arrow = diff > 0 ? '↑' : diff < 0 ? '↓' : '→'");
lines.push("    return (");
lines.push("      <div style={{display:'flex',flexDirection:'column',alignItems:'center',gap:'2px'}}>");
lines.push("        <span style={{fontSize:'11px',color:mg}}>{label}</span>");
lines.push("        <span style={{fontSize:'16px',fontWeight:'700',color:'white'}}>{current.toFixed(1)}</span>");
lines.push("        <span style={{fontSize:'11px',color,fontWeight:'600'}}>{arrow} {Math.abs(diff).toFixed(1)}</span>");
lines.push("      </div>");
lines.push("    )");
lines.push("  }");
lines.push("");
lines.push("  function ScoreButton({ value, current, onChange }: { value: number; current: number; onChange: (v: number) => void }) {");
lines.push("    const isActive = value === current");
lines.push("    return (");
lines.push("      <button onClick={() => onChange(value)} style={{width:'38px',height:'38px',borderRadius:'50%',border:isActive?'none':'1px solid '+bd,background:isActive?g:cb,color:isActive?'#000000':dg,fontSize:'14px',fontWeight:'700',cursor:'pointer'}}>");
lines.push("        {value}");
lines.push("      </button>");
lines.push("    )");
lines.push("  }");
lines.push("");
lines.push("  if (loading) return <main style={{minHeight:'100vh',background:'#000000',color:dg,display:'flex',alignItems:'center',justifyContent:'center'}}>Loading...</main>");
lines.push("");
lines.push("  return (");
lines.push("    <main style={{minHeight:'100vh',background:'#000000',color:'white',padding:'24px'}}>");
lines.push("      <div style={{maxWidth:'480px',margin:'0 auto'}}>");
lines.push("");
lines.push("        <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'20px'}}>");
lines.push("          <h1 style={{fontSize:'24px',fontWeight:'bold',color:g}}>Journal</h1>");
lines.push("          <div style={{display:'flex',gap:'8px'}}>");
lines.push("            {entries.length > 1 && <button onClick={() => setShowChart(!showChart)} style={{background:cb,color:dg,border:'1px solid '+bd,borderRadius:'6px',padding:'8px 12px',fontSize:'13px',cursor:'pointer'}}>{showChart?'Hide chart':'Show chart'}</button>}");
lines.push("            {!showForm && <button onClick={startNew} style={{background:g,color:'#000000',border:'none',borderRadius:'6px',padding:'8px 16px',fontSize:'14px',fontWeight:'700',cursor:'pointer'}}>+ Log today</button>}");
lines.push("          </div>");
lines.push("        </div>");
lines.push("");
lines.push("        {entries.length > 0 && (");
lines.push("          <div style={{background:cb,border:'1px solid '+bd,borderRadius:'12px',padding:'20px',marginBottom:'20px'}}>");
lines.push("            <div style={{display:'flex',justifyContent:'space-around',alignItems:'center',marginBottom:'16px'}}>");
lines.push("              <ProgressRing value={daysThisWeek} max={7} color={g} label='Days/week' />");
lines.push("              <ProgressRing value={thisWeek?.mood || 0} max={5} color='#3b82f6' label='Avg mood' />");
lines.push("              <ProgressRing value={thisWeek?.energy || 0} max={5} color='#f59e0b' label='Avg energy' />");
lines.push("              <ProgressRing value={thisWeek?.sleep || 0} max={9} color='#8b5cf6' label='Avg sleep' />");
lines.push("            </div>");
lines.push("            <div style={{borderTop:'1px solid '+bd,paddingTop:'12px',display:'flex',justifyContent:'center',alignItems:'center',gap:'8px'}}>");
lines.push("              <span style={{fontSize:'20px'}}>{streak >= 3 ? '🔥' : '📅'}</span>");
lines.push("              <span style={{fontSize:'14px',fontWeight:'700',color:streak >= 3 ? g : dg}}>{streak} day streak</span>");
lines.push("              {streak >= 7 && <span style={{fontSize:'11px',color:mg,background:'#0a1a0a',padding:'2px 8px',borderRadius:'4px'}}>WEEKLY GOAL HIT</span>}");
lines.push("            </div>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {thisWeek && lastWeek && (");
lines.push("          <div style={{background:cb,border:'1px solid '+bd,borderRadius:'12px',padding:'16px',marginBottom:'20px'}}>");
lines.push("            <p style={{fontSize:'12px',color:mg,marginBottom:'12px',textAlign:'center'}}>THIS WEEK VS LAST WEEK</p>");
lines.push("            <div style={{display:'flex',justifyContent:'space-around'}}>");
lines.push("              <Delta current={thisWeek.mood} previous={lastWeek.mood} label='Mood' />");
lines.push("              <Delta current={thisWeek.energy} previous={lastWeek.energy} label='Energy' />");
lines.push("              <Delta current={thisWeek.sleep} previous={lastWeek.sleep} label='Sleep' />");
lines.push("            </div>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {showChart && chartData.length > 1 && (");
lines.push("          <div style={{background:cb,border:'1px solid '+bd,borderRadius:'12px',padding:'16px',marginBottom:'20px'}}>");
lines.push("            <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'16px'}}>");
lines.push("              <h2 style={{fontSize:'14px',fontWeight:'600',color:g}}>Trends</h2>");
lines.push("              <div style={{display:'flex',gap:'4px'}}>");
lines.push("                {([7,30,'all'] as const).map(r => (");
lines.push("                  <button key={r} onClick={() => setChartRange(r)} style={{background:chartRange===r?'#0a1a0a':'none',border:'1px solid '+(chartRange===r?mg:bd),color:chartRange===r?dg:mg,fontSize:'11px',padding:'3px 8px',borderRadius:'4px',cursor:'pointer'}}>{r === 'all' ? 'All' : r+'d'}</button>");
lines.push("                ))}");
lines.push("              </div>");
lines.push("            </div>");
lines.push("            <p style={{fontSize:'11px',color:mg,marginBottom:'8px'}}>Mood & Energy (1-5)</p>");
lines.push("            <ResponsiveContainer width='100%' height={120}>");
lines.push("              <LineChart data={chartData}>");
lines.push("                <XAxis dataKey='date' tick={{fontSize:10,fill:mg}} />");
lines.push("                <YAxis domain={[1,5]} tick={{fontSize:10,fill:mg}} width={20} />");
lines.push("                <Tooltip {...tooltipStyle} />");
lines.push("                <Line type='monotone' dataKey='mood' stroke={g} strokeWidth={2} dot={false} name='Mood' />");
lines.push("                <Line type='monotone' dataKey='energy' stroke={dg} strokeWidth={2} dot={false} name='Energy' />");
lines.push("              </LineChart>");
lines.push("            </ResponsiveContainer>");
lines.push("            <p style={{fontSize:'11px',color:mg,marginBottom:'8px',marginTop:'16px'}}>Sleep (hours)</p>");
lines.push("            <ResponsiveContainer width='100%' height={100}>");
lines.push("              <LineChart data={chartData}>");
lines.push("                <XAxis dataKey='date' tick={{fontSize:10,fill:mg}} />");
lines.push("                <YAxis tick={{fontSize:10,fill:mg}} width={20} />");
lines.push("                <Tooltip {...tooltipStyle} />");
lines.push("                <Line type='monotone' dataKey='sleep' stroke='#7fff7f' strokeWidth={2} dot={false} name='Sleep' />");
lines.push("              </LineChart>");
lines.push("            </ResponsiveContainer>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {showForm && (");
lines.push("          <div style={{background:cb,border:'1px solid '+bd,borderRadius:'8px',padding:'20px',marginBottom:'24px'}}>");
lines.push("            <h2 style={{fontSize:'16px',fontWeight:'600',marginBottom:'16px',color:g}}>{editingId ? 'Edit Entry' : 'How are you today?'}</h2>");
lines.push("            <div style={{marginBottom:'16px'}}>");
lines.push("              <label style={{display:'block',fontSize:'13px',color:dg,marginBottom:'8px'}}>Mood — {moodLabel(mood)}</label>");
lines.push("              <div style={{display:'flex',gap:'8px'}}>{[1,2,3,4,5].map(v => <ScoreButton key={v} value={v} current={mood} onChange={setMood} />)}</div>");
lines.push("            </div>");
lines.push("            <div style={{marginBottom:'16px'}}>");
lines.push("              <label style={{display:'block',fontSize:'13px',color:dg,marginBottom:'8px'}}>Energy — {moodLabel(energy)}</label>");
lines.push("              <div style={{display:'flex',gap:'8px'}}>{[1,2,3,4,5].map(v => <ScoreButton key={v} value={v} current={energy} onChange={setEnergy} />)}</div>");
lines.push("            </div>");
lines.push("            <div style={{marginBottom:'16px'}}>");
lines.push("              <label style={{display:'block',fontSize:'13px',color:dg,marginBottom:'4px'}}>Hours of sleep</label>");
lines.push("              <input type='number' min='0' max='24' step='0.5' value={sleep} onChange={e => setSleep(e.target.value)} placeholder='e.g. 7.5' style={{width:'100%',background:'#000000',border:'1px solid '+bd,borderRadius:'6px',padding:'8px 10px',color:'white',fontSize:'14px',boxSizing:'border-box'}} />");
lines.push("            </div>");
lines.push("            <div style={{marginBottom:'16px'}}>");
lines.push("              <label style={{display:'block',fontSize:'13px',color:dg,marginBottom:'4px'}}>Notes (optional)</label>");
lines.push("              <textarea value={notes} onChange={e => setNotes(e.target.value)} placeholder='How do you feel? Any observations...' rows={3} style={{width:'100%',background:'#000000',border:'1px solid '+bd,borderRadius:'6px',padding:'8px 10px',color:'white',fontSize:'14px',boxSizing:'border-box',resize:'none'}} />");
lines.push("            </div>");
lines.push("            {error && <div style={{background:'#1a0000',border:'1px solid #4a0000',borderRadius:'6px',padding:'10px',fontSize:'13px',color:'#ff6b6b',marginBottom:'12px'}}>{error}</div>}");
lines.push("            <div style={{display:'flex',gap:'8px'}}>");
lines.push("              <button onClick={() => { setShowForm(false); setError(''); setEditingId(null) }} style={{flex:1,background:cb,color:dg,border:'1px solid '+bd,borderRadius:'6px',padding:'10px',fontSize:'14px',cursor:'pointer'}}>Cancel</button>");
lines.push("              <button onClick={saveEntry} disabled={saving} style={{flex:2,background:saving?'#1a3d1a':g,color:saving?mg:'#000000',border:'none',borderRadius:'6px',padding:'10px',fontSize:'14px',fontWeight:'700',cursor:saving?'not-allowed':'pointer'}}>{saving?'Saving...':editingId?'Save Changes':'Save Entry'}</button>");
lines.push("            </div>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {!loading && entries.length === 0 && !showForm && (");
lines.push("          <div style={{textAlign:'center',padding:'48px 0'}}>");
lines.push("            <p style={{color:dg,marginBottom:'8px'}}>No entries yet.</p>");
lines.push("            <p style={{color:mg,fontSize:'13px'}}>Tap + Log today to record your first entry.</p>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {sortedEntries.map((entry) => (");
lines.push("          <div key={entry.id} style={{background:cb,border:'1px solid '+bd,borderRadius:'8px',padding:'16px',marginBottom:'12px'}}>");
lines.push("            <div style={{display:'flex',justifyContent:'space-between',alignItems:'flex-start',marginBottom:'10px'}}>");
lines.push("              <span style={{fontSize:'14px',fontWeight:'600',color:g}}>{formatDate(entry.date)}</span>");
lines.push("              <div style={{display:'flex',gap:'8px'}}>");
lines.push("                <button onClick={() => startEdit(entry)} style={{background:'none',border:'none',color:dg,cursor:'pointer',fontSize:'12px'}}>Edit</button>");
lines.push("                <button onClick={() => deleteEntry(entry.id)} style={{background:'none',border:'none',color:mg,cursor:'pointer',fontSize:'12px'}}>Delete</button>");
lines.push("              </div>");
lines.push("            </div>");
lines.push("            <div style={{display:'flex',gap:'16px',marginBottom:'8px'}}>");
lines.push("              <div><span style={{fontSize:'11px',color:mg,display:'block'}}>MOOD</span><span style={{fontSize:'14px',fontWeight:'500'}}>{moodLabel(entry.mood)}</span></div>");
lines.push("              <div><span style={{fontSize:'11px',color:mg,display:'block'}}>ENERGY</span><span style={{fontSize:'14px',fontWeight:'500'}}>{moodLabel(entry.energy)}</span></div>");
lines.push("              <div><span style={{fontSize:'11px',color:mg,display:'block'}}>SLEEP</span><span style={{fontSize:'14px',fontWeight:'500'}}>{entry.sleep}h</span></div>");
lines.push("            </div>");
lines.push("            {entry.notes && <p style={{fontSize:'13px',color:dg,marginTop:'8px',lineHeight:'1.5'}}>{entry.notes}</p>}");
lines.push("          </div>");
lines.push("        ))}");
lines.push("      </div>");
lines.push("    </main>");
lines.push("  )");
lines.push("}");
fs.writeFileSync('app/journal/page.tsx', lines.join('\n'), 'utf8');
console.log('Done');
