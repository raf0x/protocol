const fs = require('fs');
const lines = [];
lines.push("export const runtime = 'nodejs'");
lines.push("");
lines.push("import { createClient } from '@supabase/supabase-js'");
lines.push("import { NextResponse } from 'next/server'");
lines.push("import type { NextRequest } from 'next/server'");
lines.push("import webpush from 'web-push'");
lines.push("");
lines.push("webpush.setVapidDetails(");
lines.push("  process.env.VAPID_EMAIL!,");
lines.push("  process.env.NEXT_PUBLIC_VAPID_PUBLIC_KEY!,");
lines.push("  process.env.VAPID_PRIVATE_KEY!,");
lines.push(")");
lines.push("");
lines.push("export async function GET(request: NextRequest) {");
lines.push("  const auth = request.headers.get('authorization')");
lines.push("  if (auth !== `Bearer ${process.env.CRON_SECRET}`) {");
lines.push("    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })");
lines.push("  }");
lines.push("  const supabase = createClient(");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_URL!,");
lines.push("    process.env.SUPABASE_SERVICE_ROLE_KEY!,");
lines.push("  )");
lines.push("  const now = new Date()");
lines.push("  const currentHour = now.getHours()");
lines.push("  const { data: subs } = await supabase");
lines.push("    .from('push_subscriptions')");
lines.push("    .select('*')");
lines.push("    .eq('reminder_hour', currentHour)");
lines.push("  if (!subs || subs.length === 0) return NextResponse.json({ sent: 0 })");
lines.push("  let sent = 0");
lines.push("  for (const sub of subs) {");
lines.push("    try {");
lines.push("      await webpush.sendNotification(");
lines.push("        sub.subscription,");
lines.push("        JSON.stringify({");
lines.push("          title: 'Protocol',");
lines.push("          body: 'Time to log your journal entry for today.',");
lines.push("          url: '/journal',");
lines.push("        })");
lines.push("      )");
lines.push("      sent++");
lines.push("    } catch (e) {");
lines.push("      console.error('Push failed:', e)");
lines.push("    }");
lines.push("  }");
lines.push("  return NextResponse.json({ sent })");
lines.push("}");
fs.writeFileSync('app/api/cron/route.ts', lines.join('\n'), 'utf8');
console.log('Done');
