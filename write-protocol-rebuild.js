const fs = require('fs');
const lines = [];
lines.push("'use client'");
lines.push("");
lines.push("import { useState, useEffect } from 'react'");
lines.push("import { createClient } from '../../lib/supabase'");
lines.push("");
lines.push("type Phase = { id?: string; name: string; dose: string; dose_unit: string; syringe_units: string; volume_ml: string; start_week: string; end_week: string; frequency: string; day_of_week: string }");
lines.push("type Compound = { id?: string; name: string; vial_strength: string; vial_unit: string; bac_water_ml: string; notes: string; phases: Phase[] }");
lines.push("type Protocol = { id: string; name: string; notes: string; start_date: string; status: string; compounds: any[] }");
lines.push("");
lines.push("const DAYS = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat']");
lines.push("const FREQS = [");
lines.push("  { value: 'daily', label: 'Daily (7x/wk)' },");
lines.push("  { value: '6x/week', label: '6x/week' },");
lines.push("  { value: '5x/week', label: '5x/week' },");
lines.push("  { value: '4x/week', label: '4x/week' },");
lines.push("  { value: '3x/week', label: '3x/week' },");
lines.push("  { value: '2x/week', label: '2x/week' },");
lines.push("  { value: '1x/week', label: '1x/week (weekly)' },");
lines.push("  { value: 'eod', label: 'Every other day' },");
lines.push("  { value: 'every3days', label: 'Every 3 days' },");
lines.push("  { value: 'every4days', label: 'Every 4 days' },");
lines.push("  { value: 'every5days', label: 'Every 5 days' },");
lines.push("]");
lines.push("");
lines.push("function newPhase(): Phase { return { name: '', dose: '', dose_unit: 'mg', syringe_units: '', volume_ml: '', start_week: '1', end_week: '1', frequency: '1x/week', day_of_week: '0' } }");
lines.push("function newCompound(): Compound { return { name: '', vial_strength: '', vial_unit: 'mg', bac_water_ml: '', notes: '', phases: [newPhase()] } }");
lines.push("");
lines.push("export default function ProtocolPage() {");
lines.push("  const [protocols, setProtocols] = useState<Protocol[]>([])");
lines.push("  const [loading, setLoading] = useState(true)");
lines.push("  const [showForm, setShowForm] = useState(false)");
lines.push("  const [editingId, setEditingId] = useState<string | null>(null)");
lines.push("  const [protocolName, setProtocolName] = useState('')");
lines.push("  const [notes, setNotes] = useState('')");
lines.push("  const today = new Date().toISOString().split('T')[0]");
lines.push("  const [startDate, setStartDate] = useState(today)");
lines.push("  const [compounds, setCompounds] = useState<Compound[]>([newCompound()])");
lines.push("  const [saving, setSaving] = useState(false)");
lines.push("  const [error, setError] = useState('')");
lines.push("");
lines.push("  const g = '#39ff14'");
lines.push("  const dg = '#8b8ba7'");
lines.push("  const mg = '#3d3d5c'");
lines.push("  const cb = '#12121a'");
lines.push("  const bd = '#1e1e2e'");
lines.push("");
lines.push("  useEffect(() => { loadProtocols() }, [])");
lines.push("");
lines.push("  async function loadProtocols() {");
lines.push("    setLoading(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setLoading(false); return }");
lines.push("    const { data } = await supabase.from('protocols').select('*, compounds(*, phases(*))').order('created_at', { ascending: false })");
lines.push("    setProtocols(data || [])");
lines.push("    setLoading(false)");
lines.push("  }");
lines.push("");
lines.push("  function startNew() {");
lines.push("    setEditingId(null)");
lines.push("    setProtocolName('')");
lines.push("    setNotes('')");
lines.push("    setStartDate(today)");
lines.push("    setCompounds([newCompound()])");
lines.push("    setShowForm(true)");
lines.push("    setError('')");
lines.push("  }");
lines.push("");
lines.push("  function startEdit(p: any) {");
lines.push("    setEditingId(p.id)");
lines.push("    setProtocolName(p.name)");
lines.push("    setNotes(p.notes || '')");
lines.push("    setStartDate(p.start_date)");
lines.push("    const cs = (p.compounds || []).map((c: any) => ({");
lines.push("      id: c.id,");
lines.push("      name: c.name,");
lines.push("      vial_strength: c.vial_strength?.toString() || '',");
lines.push("      vial_unit: c.vial_unit || 'mg',");
lines.push("      bac_water_ml: c.bac_water_ml?.toString() || '',");
lines.push("      notes: c.notes || '',");
lines.push("      phases: (c.phases || []).sort((a: any, b: any) => a.start_week - b.start_week).map((ph: any) => ({");
lines.push("        id: ph.id,");
lines.push("        name: ph.name,");
lines.push("        dose: ph.dose?.toString() || '',");
lines.push("        dose_unit: ph.dose_unit || 'mg',");
lines.push("        syringe_units: ph.syringe_units?.toString() || '',");
lines.push("        volume_ml: ph.volume_ml?.toString() || '',");
lines.push("        start_week: ph.start_week?.toString() || '1',");
lines.push("        end_week: ph.end_week?.toString() || '1',");
lines.push("        frequency: ph.frequency || '1x/week',");
lines.push("        day_of_week: ph.day_of_week?.toString() || '0'");
lines.push("      }))");
lines.push("    }))");
lines.push("    setCompounds(cs.length ? cs : [newCompound()])");
lines.push("    setShowForm(true)");
lines.push("    setError('')");
lines.push("  }");
lines.push("");
lines.push("  function addCompound() { setCompounds([...compounds, newCompound()]) }");
lines.push("  function removeCompound(i: number) { setCompounds(compounds.filter((_, idx) => idx !== i)) }");
lines.push("  function updateCompound(i: number, field: keyof Compound, value: any) {");
lines.push("    const u = [...compounds]; (u[i] as any)[field] = value; setCompounds(u)");
lines.push("  }");
lines.push("");
lines.push("  function addPhase(ci: number) {");
lines.push("    const u = [...compounds]");
lines.push("    const lastPhase = u[ci].phases[u[ci].phases.length - 1]");
lines.push("    const nextStart = lastPhase ? (parseInt(lastPhase.end_week) + 1).toString() : '1'");
lines.push("    const newP = { ...newPhase(), start_week: nextStart, end_week: nextStart }");
lines.push("    u[ci].phases.push(newP)");
lines.push("    setCompounds(u)");
lines.push("  }");
lines.push("  function removePhase(ci: number, pi: number) {");
lines.push("    const u = [...compounds]");
lines.push("    u[ci].phases = u[ci].phases.filter((_, idx) => idx !== pi)");
lines.push("    if (u[ci].phases.length === 0) u[ci].phases.push(newPhase())");
lines.push("    setCompounds(u)");
lines.push("  }");
lines.push("  function updatePhase(ci: number, pi: number, field: keyof Phase, value: string) {");
lines.push("    const u = [...compounds]; (u[ci].phases[pi] as any)[field] = value; setCompounds(u)");
lines.push("  }");
lines.push("");
lines.push("  async function saveProtocol() {");
lines.push("    setError('')");
lines.push("    if (compounds.some(c => !c.name.trim())) { setError('Every compound needs a name.'); return }");
lines.push("    if (compounds.some(c => c.phases.some(p => !p.dose.trim()))) { setError('Every phase needs a dose.'); return }");
lines.push("    setSaving(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setError('You must be signed in.'); setSaving(false); return }");
lines.push("    const firstName = protocolName.trim() || compounds[0].name.trim()");
lines.push("");
lines.push("    let protocolId = editingId");
lines.push("    if (editingId) {");
lines.push("      await supabase.from('protocols').update({ name: firstName, notes: notes.trim(), start_date: startDate }).eq('id', editingId)");
lines.push("      await supabase.from('compounds').delete().eq('protocol_id', editingId)");
lines.push("    } else {");
lines.push("      const { data: p, error: e } = await supabase.from('protocols').insert({ user_id: user.id, name: firstName, notes: notes.trim(), start_date: startDate }).select().single()");
lines.push("      if (e) { setError(e.message); setSaving(false); return }");
lines.push("      protocolId = p.id");
lines.push("    }");
lines.push("");
lines.push("    for (let ci = 0; ci < compounds.length; ci++) {");
lines.push("      const c = compounds[ci]");
lines.push("      const { data: ins, error: ce } = await supabase.from('compounds').insert({");
lines.push("        protocol_id: protocolId, user_id: user.id, name: c.name.trim(),");
lines.push("        vial_strength: c.vial_strength ? parseFloat(c.vial_strength) : null,");
lines.push("        vial_unit: c.vial_unit,");
lines.push("        bac_water_ml: c.bac_water_ml ? parseFloat(c.bac_water_ml) : null,");
lines.push("        notes: c.notes.trim(), position: ci");
lines.push("      }).select().single()");
lines.push("      if (ce) { setError(ce.message); setSaving(false); return }");
lines.push("      const compoundId = ins.id");
lines.push("      const phaseRows = c.phases.map((ph, pi) => ({");
lines.push("        compound_id: compoundId, user_id: user.id,");
lines.push("        name: ph.name.trim() || ('Phase ' + (pi + 1)),");
lines.push("        dose: parseFloat(ph.dose),");
lines.push("        dose_unit: ph.dose_unit,");
lines.push("        syringe_units: ph.syringe_units ? parseFloat(ph.syringe_units) : null,");
lines.push("        volume_ml: ph.volume_ml ? parseFloat(ph.volume_ml) : null,");
lines.push("        start_week: parseInt(ph.start_week) || 1,");
lines.push("        end_week: parseInt(ph.end_week) || 1,");
lines.push("        frequency: ph.frequency,");
lines.push("        day_of_week: ph.day_of_week ? parseInt(ph.day_of_week) : null,");
lines.push("        position: pi");
lines.push("      }))");
lines.push("      if (phaseRows.length) await supabase.from('phases').insert(phaseRows)");
lines.push("    }");
lines.push("");
lines.push("    setShowForm(false); setEditingId(null); setSaving(false)");
lines.push("    loadProtocols()");
lines.push("  }");
lines.push("");
lines.push("  async function deleteProtocol(id: string) {");
lines.push("    const supabase = createClient()");
lines.push("    await supabase.from('protocols').delete().eq('id', id)");
lines.push("    loadProtocols()");
lines.push("  }");
lines.push("");
lines.push("  const inputStyle = { width:'100%', background:'#0a0a0f', border:'1px solid '+bd, borderRadius:'6px', padding:'8px 10px', color:'white', fontSize:'14px', boxSizing:'border-box' as const, colorScheme:'dark' as const }");
lines.push("  const smallInputStyle = { ...inputStyle, fontSize:'13px', padding:'6px 8px' }");
lines.push("");
lines.push("  return (");
lines.push("    <main style={{minHeight:'100vh',color:'white',padding:'24px'}}>");
lines.push("      <div style={{maxWidth:'540px',margin:'0 auto'}}>");
lines.push("        <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'24px'}}>");
lines.push("          <h1 style={{fontSize:'24px',fontWeight:'bold',color:g}}>My Protocols</h1>");
lines.push("          {!showForm && <button onClick={startNew} style={{background:g,color:'#000',border:'none',borderRadius:'6px',padding:'8px 16px',fontSize:'14px',fontWeight:'700',cursor:'pointer'}}>+ New</button>}");
lines.push("        </div>");
lines.push("");
lines.push("        {showForm && (");
lines.push("          <div style={{background:cb,border:'1px solid '+bd,borderRadius:'12px',padding:'20px',marginBottom:'24px'}}>");
lines.push("            <h2 style={{fontSize:'16px',fontWeight:'700',marginBottom:'16px',color:g}}>{editingId ? 'Edit Protocol' : 'New Protocol'}</h2>");
lines.push("");
lines.push("            <div style={{marginBottom:'14px'}}>");
lines.push("              <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'4px',fontWeight:'600'}}>PROTOCOL NAME (optional)</label>");
lines.push("              <input value={protocolName} onChange={e => setProtocolName(e.target.value)} placeholder='Auto-fills from first compound' style={inputStyle} />");
lines.push("            </div>");
lines.push("");
lines.push("            <div style={{marginBottom:'14px'}}>");
lines.push("              <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'4px',fontWeight:'600'}}>START DATE</label>");
lines.push("              <input type='date' value={startDate} onChange={e => setStartDate(e.target.value)} style={inputStyle} />");
lines.push("            </div>");
lines.push("");
lines.push("            <div style={{marginBottom:'20px'}}>");
lines.push("              <label style={{display:'block',fontSize:'12px',color:'#ffffff',marginBottom:'10px',fontWeight:'700',letterSpacing:'1px'}}>COMPOUNDS</label>");
lines.push("              {compounds.map((c, ci) => (");
lines.push("                <div key={ci} style={{background:'#0a0a0f',border:'1px solid '+bd,borderRadius:'8px',padding:'14px',marginBottom:'10px'}}>");
lines.push("                  <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'10px'}}>");
lines.push("                    <span style={{fontSize:'12px',color:mg,fontWeight:'700'}}>COMPOUND {ci + 1}</span>");
lines.push("                    {compounds.length > 1 && <button onClick={() => removeCompound(ci)} style={{background:'none',border:'none',color:'#ff6b6b',cursor:'pointer',fontSize:'12px'}}>Remove</button>}");
lines.push("                  </div>");
lines.push("                  <input value={c.name} onChange={e => updateCompound(ci, 'name', e.target.value)} placeholder='Compound name (e.g. Retatrutide)' style={{...inputStyle,marginBottom:'8px'}} />");
lines.push("                  <div style={{display:'flex',gap:'6px',marginBottom:'8px'}}>");
lines.push("                    <input type='number' value={c.vial_strength} onChange={e => updateCompound(ci, 'vial_strength', e.target.value)} placeholder='Vial mg' style={smallInputStyle} />");
lines.push("                    <input type='number' value={c.bac_water_ml} onChange={e => updateCompound(ci, 'bac_water_ml', e.target.value)} placeholder='BAC water mL' style={smallInputStyle} />");
lines.push("                  </div>");
lines.push("");
lines.push("                  <div style={{marginTop:'12px',paddingTop:'12px',borderTop:'1px solid '+bd}}>");
lines.push("                    <span style={{fontSize:'11px',color:dg,fontWeight:'700',letterSpacing:'1px'}}>PHASES</span>");
lines.push("                    {c.phases.map((ph, pi) => (");
lines.push("                      <div key={pi} style={{background:cb,border:'1px solid '+bd,borderRadius:'6px',padding:'10px',marginTop:'8px'}}>");
lines.push("                        <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'8px'}}>");
lines.push("                          <input value={ph.name} onChange={e => updatePhase(ci, pi, 'name', e.target.value)} placeholder={'Phase ' + (pi+1) + ' name'} style={{...smallInputStyle,maxWidth:'160px'}} />");
lines.push("                          {c.phases.length > 1 && <button onClick={() => removePhase(ci, pi)} style={{background:'none',border:'none',color:'#ff6b6b',cursor:'pointer',fontSize:'11px'}}>×</button>}");
lines.push("                        </div>");
lines.push("                        <div style={{display:'grid',gridTemplateColumns:'1fr 1fr',gap:'6px',marginBottom:'6px'}}>");
lines.push("                          <input type='number' step='0.01' value={ph.dose} onChange={e => updatePhase(ci, pi, 'dose', e.target.value)} placeholder='Dose' style={smallInputStyle} />");
lines.push("                          <select value={ph.dose_unit} onChange={e => updatePhase(ci, pi, 'dose_unit', e.target.value)} style={smallInputStyle}>");
lines.push("                            <option value='mg'>mg</option>");
lines.push("                            <option value='mcg'>mcg</option>");
lines.push("                            <option value='IU'>IU</option>");
lines.push("                          </select>");
lines.push("                        </div>");
lines.push("                        <div style={{display:'grid',gridTemplateColumns:'1fr 1fr',gap:'6px',marginBottom:'6px'}}>");
lines.push("                          <input type='number' value={ph.syringe_units} onChange={e => updatePhase(ci, pi, 'syringe_units', e.target.value)} placeholder='Syringe units' style={smallInputStyle} />");
lines.push("                          <input type='number' step='0.01' value={ph.volume_ml} onChange={e => updatePhase(ci, pi, 'volume_ml', e.target.value)} placeholder='Volume mL' style={smallInputStyle} />");
lines.push("                        </div>");
lines.push("                        <div style={{display:'grid',gridTemplateColumns:'1fr 1fr',gap:'6px',marginBottom:'6px'}}>");
lines.push("                          <input type='number' min='1' value={ph.start_week} onChange={e => updatePhase(ci, pi, 'start_week', e.target.value)} placeholder='Start week' style={smallInputStyle} />");
lines.push("                          <input type='number' min='1' value={ph.end_week} onChange={e => updatePhase(ci, pi, 'end_week', e.target.value)} placeholder='End week' style={smallInputStyle} />");
lines.push("                        </div>");
lines.push("                        <select value={ph.frequency} onChange={e => updatePhase(ci, pi, 'frequency', e.target.value)} style={{...smallInputStyle,marginBottom:'6px'}}>");
lines.push("                          {FREQS.map(f => <option key={f.value} value={f.value}>{f.label}</option>)}");
lines.push("                        </select>");
lines.push("                        {(ph.frequency === '1x/week' || ph.frequency === '2x/week') && (");
lines.push("                          <select value={ph.day_of_week} onChange={e => updatePhase(ci, pi, 'day_of_week', e.target.value)} style={smallInputStyle}>");
lines.push("                            {DAYS.map((d, i) => <option key={i} value={i}>Inject on {d}</option>)}");
lines.push("                          </select>");
lines.push("                        )}");
lines.push("                      </div>");
lines.push("                    ))}");
lines.push("                    <button onClick={() => addPhase(ci)} style={{marginTop:'8px',background:'none',border:'1px dashed '+mg,borderRadius:'6px',padding:'6px 10px',color:dg,fontSize:'12px',cursor:'pointer',width:'100%'}}>+ Add phase</button>");
lines.push("                  </div>");
lines.push("                </div>");
lines.push("              ))}");
lines.push("              <button onClick={addCompound} style={{background:'none',border:'1px dashed '+mg,borderRadius:'8px',padding:'10px',width:'100%',color:dg,fontSize:'13px',cursor:'pointer'}}>+ Add another compound</button>");
lines.push("            </div>");
lines.push("");
lines.push("            <div style={{marginBottom:'16px'}}>");
lines.push("              <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'4px',fontWeight:'600'}}>NOTES (optional)</label>");
lines.push("              <textarea value={notes} onChange={e => setNotes(e.target.value)} placeholder='Goals, context, observations...' rows={2} style={{...inputStyle,resize:'none'}} />");
lines.push("            </div>");
lines.push("");
lines.push("            {error && <div style={{background:'#1a0000',border:'1px solid #4a0000',borderRadius:'6px',padding:'10px',fontSize:'13px',color:'#ff6b6b',marginBottom:'12px'}}>{error}</div>}");
lines.push("");
lines.push("            <div style={{display:'flex',gap:'8px'}}>");
lines.push("              <button onClick={() => { setShowForm(false); setError(''); setEditingId(null) }} style={{flex:1,background:cb,color:dg,border:'1px solid '+bd,borderRadius:'6px',padding:'10px',fontSize:'14px',cursor:'pointer'}}>Cancel</button>");
lines.push("              <button onClick={saveProtocol} disabled={saving} style={{flex:2,background:saving?'#1a3d1a':g,color:saving?mg:'#000',border:'none',borderRadius:'6px',padding:'10px',fontSize:'14px',fontWeight:'700',cursor:saving?'not-allowed':'pointer'}}>{saving?'Saving...':editingId?'Save Changes':'Save Protocol'}</button>");
lines.push("            </div>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {loading && <p style={{color:mg}}>Loading...</p>}");
lines.push("        {!loading && protocols.length === 0 && !showForm && (");
lines.push("          <div style={{textAlign:'center',padding:'48px 0'}}>");
lines.push("            <p style={{color:dg,marginBottom:'8px'}}>No protocols yet.</p>");
lines.push("            <p style={{color:mg,fontSize:'13px'}}>Tap + New to build your first one with phases.</p>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {protocols.map((p: any) => {");
lines.push("          const startMs = new Date(p.start_date + 'T00:00:00').getTime()");
lines.push("          const nowMs = Date.now()");
lines.push("          const daysIn = Math.floor((nowMs - startMs) / (86400000))");
lines.push("          const currentWeek = Math.max(1, Math.floor(daysIn / 7) + 1)");
lines.push("          return (");
lines.push("            <div key={p.id} style={{background:cb,border:'1px solid '+bd,borderRadius:'12px',padding:'16px',marginBottom:'12px'}}>");
lines.push("              <div style={{display:'flex',justifyContent:'space-between',alignItems:'flex-start',marginBottom:'10px'}}>");
lines.push("                <div>");
lines.push("                  <h2 style={{fontSize:'17px',fontWeight:'700',color:g}}>{p.name}</h2>");
lines.push("                  <p style={{fontSize:'12px',color:mg,marginTop:'2px'}}>Week {currentWeek} · Started {new Date(p.start_date + 'T12:00:00').toLocaleDateString('en-US',{month:'short',day:'numeric'})}</p>");
lines.push("                </div>");
lines.push("                <div style={{display:'flex',gap:'8px'}}>");
lines.push("                  <button onClick={() => startEdit(p)} style={{background:'none',border:'none',color:dg,cursor:'pointer',fontSize:'12px'}}>Edit</button>");
lines.push("                  <button onClick={() => deleteProtocol(p.id)} style={{background:'none',border:'none',color:mg,cursor:'pointer',fontSize:'12px'}}>Delete</button>");
lines.push("                </div>");
lines.push("              </div>");
lines.push("              {p.notes && <p style={{color:dg,fontSize:'13px',marginBottom:'10px'}}>{p.notes}</p>}");
lines.push("              {(p.compounds || []).map((c: any) => (");
lines.push("                <div key={c.id} style={{background:'#0a0a0f',borderRadius:'8px',padding:'10px',marginTop:'8px'}}>");
lines.push("                  <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'6px'}}>");
lines.push("                    <span style={{fontSize:'14px',fontWeight:'700',color:'white'}}>{c.name}</span>");
lines.push("                    {c.vial_strength && <span style={{fontSize:'11px',color:mg}}>{c.vial_strength}{c.vial_unit} vial · {c.bac_water_ml || '?'}mL BAC</span>}");
lines.push("                  </div>");
lines.push("                  {(c.phases || []).sort((a: any, b: any) => a.start_week - b.start_week).map((ph: any) => {");
lines.push("                    const isCurrent = currentWeek >= ph.start_week && currentWeek <= ph.end_week");
lines.push("                    return (");
lines.push("                      <div key={ph.id} style={{display:'flex',justifyContent:'space-between',padding:'5px 0',fontSize:'12px',color:isCurrent?g:dg}}>");
lines.push("                        <span>{isCurrent && '→ '}{ph.name} · W{ph.start_week}-{ph.end_week}</span>");
lines.push("                        <span style={{color:isCurrent?g:mg}}>{ph.dose}{ph.dose_unit}{ph.syringe_units?' · '+ph.syringe_units+'u':''}</span>");
lines.push("                      </div>");
lines.push("                    )");
lines.push("                  })}");
lines.push("                </div>");
lines.push("              ))}");
lines.push("            </div>");
lines.push("          )");
lines.push("        })}");
lines.push("      </div>");
lines.push("    </main>");
lines.push("  )");
