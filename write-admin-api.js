const fs = require('fs');
const lines = [];
lines.push("import { createClient } from '@supabase/supabase-js'");
lines.push("import { NextResponse } from 'next/server'");
lines.push("import type { NextRequest } from 'next/server'");
lines.push("");
lines.push("const ADMIN_ID = '41266062-c8a7-4a52-aa9b-c1fb96d1c483'");
lines.push("");
lines.push("export async function GET(request: NextRequest) {");
lines.push("  const authHeader = request.headers.get('x-user-id')");
lines.push("  if (authHeader !== ADMIN_ID) {");
lines.push("    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })");
lines.push("  }");
lines.push("  const supabase = createClient(");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_URL!,");
lines.push("    process.env.SUPABASE_SERVICE_ROLE_KEY!,");
lines.push("  )");
lines.push("  const { data, error } = await supabase");
lines.push("    .from('cohort_posts')");
lines.push("    .select('*, cohorts(name)')");
lines.push("    .order('created_at', { ascending: false })");
lines.push("  if (error) return NextResponse.json({ error: error.message }, { status: 500 })");
lines.push("  return NextResponse.json({ posts: data })");
lines.push("}");
lines.push("");
lines.push("export async function DELETE(request: NextRequest) {");
lines.push("  const authHeader = request.headers.get('x-user-id')");
lines.push("  if (authHeader !== ADMIN_ID) {");
lines.push("    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })");
lines.push("  }");
lines.push("  const { id } = await request.json()");
lines.push("  const supabase = createClient(");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_URL!,");
lines.push("    process.env.SUPABASE_SERVICE_ROLE_KEY!,");
lines.push("  )");
lines.push("  await supabase.from('cohort_posts').delete().eq('id', id)");
lines.push("  return NextResponse.json({ success: true })");
lines.push("}");
lines.push("");
lines.push("export async function PATCH(request: NextRequest) {");
lines.push("  const authHeader = request.headers.get('x-user-id')");
lines.push("  if (authHeader !== ADMIN_ID) {");
lines.push("    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })");
lines.push("  }");
lines.push("  const { id, flagged } = await request.json()");
lines.push("  const supabase = createClient(");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_URL!,");
lines.push("    process.env.SUPABASE_SERVICE_ROLE_KEY!,");
lines.push("  )");
lines.push("  await supabase.from('cohort_posts').update({ flagged }).eq('id', id)");
lines.push("  return NextResponse.json({ success: true })");
lines.push("}");
fs.writeFileSync('app/api/admin/route.ts', lines.join('\n'), 'utf8');
console.log('Done');
