const fs = require('fs');
const lines = [];
lines.push("'use client'");
lines.push("");
lines.push("import { useState, useEffect } from 'react'");
lines.push("import { createClient } from '../../lib/supabase'");
lines.push("");
lines.push("type Compound = { name: string; dose: string; unit: string; frequency: string; start_date: string }");
lines.push("type Protocol = { id: string; name: string; notes: string; compounds: any[] }");
lines.push("");
lines.push("export default function ProtocolPage() {");
lines.push("  const [protocols, setProtocols] = useState<Protocol[]>([])");
lines.push("  const [loading, setLoading] = useState(true)");
lines.push("  const [showForm, setShowForm] = useState(false)");
lines.push("  const [editingId, setEditingId] = useState<string | null>(null)");
lines.push("  const [notes, setNotes] = useState('')");
lines.push("  const today = new Date().toISOString().split('T')[0]");
lines.push("  const [compounds, setCompounds] = useState<Compound[]>([{ name: '', dose: '', unit: 'mcg', frequency: 'daily', start_date: today }])");
lines.push("  const [saving, setSaving] = useState(false)");
lines.push("  const [error, setError] = useState('')");
lines.push("  const g = '#39ff14'");
lines.push("  const dg = '#8b8ba7'");
lines.push("  const mg = '#3d3d5c'");
lines.push("  const cb = '#12121a'");
lines.push("  const bd = '#1e1e2e'");
lines.push("");
lines.push("  useEffect(() => { loadProtocols() }, [])");
lines.push("");
lines.push("  async function loadProtocols() {");
lines.push("    setLoading(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setLoading(false); return }");
lines.push("    const { data } = await supabase.from('protocols').select('*, compounds(*)').order('created_at', { ascending: false })");
lines.push("    setProtocols(data || [])");
lines.push("    setLoading(false)");
lines.push("  }");
lines.push("");
lines.push("  function addCompound() { setCompounds([...compounds, { name: '', dose: '', unit: 'mcg', frequency: 'daily', start_date: today }]) }");
lines.push("  function updateCompound(index: number, field: keyof Compound, value: string) { const u = [...compounds]; u[index][field] = value; setCompounds(u) }");
lines.push("  function removeCompound(index: number) { setCompounds(compounds.filter((_, i) => i !== index)) }");
lines.push("");
lines.push("  function startEdit(protocol: any) {");
lines.push("    setEditingId(protocol.id)");
lines.push("    setNotes(protocol.notes || '')");
lines.push("    setCompounds(protocol.compounds.map((c: any) => ({ name: c.name, dose: String(c.dose), unit: c.unit, frequency: c.frequency, start_date: c.start_date || today })))");
lines.push("    setShowForm(true)");
lines.push("    setError('')");
lines.push("  }");
lines.push("");
lines.push("  function startNew() {");
lines.push("    setEditingId(null)");
lines.push("    setNotes('')");
lines.push("    setCompounds([{ name: '', dose: '', unit: 'mcg', frequency: 'daily', start_date: today }])");
lines.push("    setShowForm(true)");
lines.push("    setError('')");
lines.push("  }");
lines.push("");
lines.push("  async function saveProtocol() {");
lines.push("    setError('')");
lines.push("    if (compounds.some(c => !c.name.trim() || !c.dose.trim())) { setError('Please fill in the name and dose for every compound.'); return }");
lines.push("    const protocolName = compounds[0].name.trim()");
lines.push("    setSaving(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setError('You must be signed in.'); setSaving(false); return }");
lines.push("");
lines.push("    if (editingId) {");
lines.push("      await supabase.from('protocols').update({ name: protocolName, notes: notes.trim() }).eq('id', editingId)");
lines.push("      await supabase.from('compounds').delete().eq('protocol_id', editingId)");
lines.push("      const rows = compounds.map(c => ({ protocol_id: editingId, user_id: user.id, name: c.name.trim(), dose: parseFloat(c.dose), unit: c.unit, frequency: c.frequency, start_date: c.start_date }))");
lines.push("      await supabase.from('compounds').insert(rows)");
lines.push("    } else {");
lines.push("      const { data: protocol, error: pe } = await supabase.from('protocols').insert({ name: protocolName, notes: notes.trim(), user_id: user.id }).select().single()");
lines.push("      if (pe) { setError(pe.message); setSaving(false); return }");
lines.push("      const rows = compounds.map(c => ({ protocol_id: protocol.id, user_id: user.id, name: c.name.trim(), dose: parseFloat(c.dose), unit: c.unit, frequency: c.frequency, start_date: c.start_date }))");
lines.push("      await supabase.from('compounds').insert(rows)");
lines.push("    }");
lines.push("");
lines.push("    setShowForm(false)");
lines.push("    setEditingId(null)");
lines.push("    setSaving(false)");
lines.push("    loadProtocols()");
lines.push("  }");
lines.push("");
lines.push("  async function deleteProtocol(id: string) {");
lines.push("    const supabase = createClient()");
lines.push("    await supabase.from('protocols').delete().eq('id', id)");
lines.push("    loadProtocols()");
lines.push("  }");
lines.push("");
lines.push("  const inputStyle = {width:'100%',background:'#0a0a0f',border:'1px solid '+bd,borderRadius:'6px',padding:'8px 10px',color:'white',fontSize:'14px',boxSizing:'border-box' as const}");
lines.push("  const selectStyle = {background:'#0a0a0f',border:'1px solid '+bd,borderRadius:'4px',padding:'6px 4px',color:'white',fontSize:'12px',maxWidth:'120px'}");
lines.push("");
lines.push("  return (");
lines.push("    <main style={{minHeight:'100vh',color:'white',padding:'24px'}}>");
lines.push("      <div style={{maxWidth:'480px',margin:'0 auto'}}>");
lines.push("        <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'24px'}}>");
lines.push("          <h1 style={{fontSize:'24px',fontWeight:'bold',color:g}}>My Protocols</h1>");
lines.push("          {!showForm && <button onClick={startNew} style={{background:g,color:'#000000',border:'none',borderRadius:'6px',padding:'8px 16px',fontSize:'14px',fontWeight:'700',cursor:'pointer'}}>+ New</button>}");
lines.push("        </div>");
lines.push("");
lines.push("        {showForm && (");
lines.push("          <div style={{background:cb,border:'1px solid '+bd,borderRadius:'8px',padding:'20px',marginBottom:'24px'}}>");
lines.push("            <h2 style={{fontSize:'16px',fontWeight:'600',marginBottom:'16px',color:g}}>{editingId ? 'Edit Protocol' : 'New Protocol'}</h2>");
lines.push("            <div style={{marginBottom:'16px'}}>");
lines.push("              <label style={{display:'block',fontSize:'13px',color:dg,marginBottom:'4px'}}>Notes (optional)</label>");
lines.push("              <textarea value={notes} onChange={e => setNotes(e.target.value)} placeholder='Goals, context, cycle length...' rows={2} style={{...inputStyle,resize:'none'}} />");
lines.push("            </div>");
lines.push("            <div style={{marginBottom:'12px'}}>");
lines.push("              <label style={{display:'block',fontSize:'13px',color:dg,marginBottom:'8px'}}>Compounds</label>");
lines.push("              {compounds.map((compound, index) => (");
lines.push("                <div key={index} style={{background:'#0a0a0f',border:'1px solid '+bd,borderRadius:'6px',padding:'12px',marginBottom:'8px'}}>");
lines.push("                  <div style={{display:'flex',justifyContent:'space-between',marginBottom:'8px'}}>");
lines.push("                    <span style={{fontSize:'12px',color:mg}}>Compound {index + 1}</span>");
lines.push("                    {compounds.length > 1 && <button onClick={() => removeCompound(index)} style={{background:'none',border:'none',color:'#ff4444',cursor:'pointer',fontSize:'12px'}}>Remove</button>}");
lines.push("                  </div>");
lines.push("                  <input value={compound.name} onChange={e => updateCompound(index, 'name', e.target.value)} placeholder='Compound name (e.g. BPC-157)' style={{...inputStyle,marginBottom:'6px'}} />");
lines.push("                  <div style={{display:'flex',gap:'6px',marginBottom:'6px'}}>");
lines.push("                    <input type='number' value={compound.dose} onChange={e => updateCompound(index, 'dose', e.target.value)} placeholder='Dose' style={{flex:1,background:'#0a0a0f',border:'1px solid '+bd,borderRadius:'4px',padding:'6px 8px',color:'white',fontSize:'13px'}} />");
lines.push("                    <select value={compound.unit} onChange={e => updateCompound(index, 'unit', e.target.value)} style={selectStyle}>");
lines.push("                      <option value='mcg'>mcg</option>");
lines.push("                      <option value='mg'>mg</option>");
lines.push("                      <option value='IU'>IU</option>");
lines.push("                      <option value='ml'>ml</option>");
lines.push("                    </select>");
lines.push("                    <select value={compound.frequency} onChange={e => updateCompound(index, 'frequency', e.target.value)} style={selectStyle}>");
lines.push("                      <option value='daily'>Daily (7x/wk)</option>");
lines.push("                      <option value='6x/week'>6x/week</option>");
lines.push("                      <option value='5x/week'>5x/week</option>");
lines.push("                      <option value='4x/week'>4x/week</option>");
lines.push("                      <option value='3x/week'>3x/week</option>");
lines.push("                      <option value='2x/week'>2x/week</option>");
lines.push("                      <option value='1x/week'>1x/week</option>");
lines.push("                      <option value='eod'>Every other day</option>");
lines.push("                      <option value='every3days'>Every 3 days</option>");
lines.push("                      <option value='every4days'>Every 4 days</option>");
lines.push("                      <option value='every5days'>Every 5 days</option>");
lines.push("                      <option value='monthly'>Monthly</option>");
lines.push("                    </select>");
lines.push("                  </div>");
lines.push("                  <div>");
lines.push("                    <label style={{display:'block',fontSize:'11px',color:mg,marginBottom:'4px'}}>Start date</label>");
lines.push("                    <input type='date' value={compound.start_date} onChange={e => updateCompound(index, 'start_date', e.target.value)} style={{width:'100%',background:'#0a0a0f',border:'1px solid '+bd,borderRadius:'4px',padding:'6px 8px',color:'white',fontSize:'13px',boxSizing:'border-box',colorScheme:'dark'}} />");
lines.push("                  </div>");
lines.push("                </div>");
lines.push("              ))}");
lines.push("              <button onClick={addCompound} style={{background:'none',border:'1px dashed #3d3d5c',borderRadius:'6px',padding:'8px',width:'100%',color:mg,fontSize:'13px',cursor:'pointer'}}>+ Add another compound</button>");
lines.push("            </div>");
lines.push("            {error && <div style={{background:'#1a0000',border:'1px solid #4a0000',borderRadius:'6px',padding:'10px',fontSize:'13px',color:'#ff6b6b',marginBottom:'12px'}}>{error}</div>}");
lines.push("            <div style={{display:'flex',gap:'8px'}}>");
lines.push("              <button onClick={() => { setShowForm(false); setError(''); setEditingId(null) }} style={{flex:1,background:cb,color:dg,border:'1px solid '+bd,borderRadius:'6px',padding:'10px',fontSize:'14px',cursor:'pointer'}}>Cancel</button>");
lines.push("              <button onClick={saveProtocol} disabled={saving} style={{flex:2,background:saving?'#1a3d1a':g,color:saving?mg:'#000000',border:'none',borderRadius:'6px',padding:'10px',fontSize:'14px',fontWeight:'700',cursor:saving?'not-allowed':'pointer'}}>{saving?'Saving...': editingId ? 'Save Changes' : 'Save Protocol'}</button>");
lines.push("            </div>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {loading && <p style={{color:mg,fontSize:'14px'}}>Loading...</p>}");
lines.push("        {!loading && protocols.length === 0 && !showForm && (");
lines.push("          <div style={{textAlign:'center',padding:'48px 0'}}>");
lines.push("            <p style={{color:dg,marginBottom:'8px'}}>No protocols yet.</p>");
lines.push("            <p style={{color:mg,fontSize:'13px'}}>Tap + New to create your first one.</p>");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {protocols.map((protocol: any) => (");
lines.push("          <div key={protocol.id} style={{background:cb,border:'1px solid '+bd,borderRadius:'8px',padding:'16px',marginBottom:'12px'}}>");
lines.push("            <div style={{display:'flex',justifyContent:'space-between',alignItems:'flex-start',marginBottom:'8px'}}>");
lines.push("              <h2 style={{fontSize:'16px',fontWeight:'600',color:g}}>{protocol.name}</h2>");
lines.push("              <div style={{display:'flex',gap:'8px'}}>");
lines.push("                <button onClick={() => startEdit(protocol)} style={{background:'none',border:'none',color:dg,cursor:'pointer',fontSize:'12px'}}>Edit</button>");
lines.push("                <button onClick={() => deleteProtocol(protocol.id)} style={{background:'none',border:'none',color:mg,cursor:'pointer',fontSize:'12px'}}>Delete</button>");
lines.push("              </div>");
lines.push("            </div>");
lines.push("            {protocol.notes && <p style={{color:dg,fontSize:'13px',marginBottom:'10px'}}>{protocol.notes}</p>}");
lines.push("            {protocol.compounds && protocol.compounds.map((c: any, i: number) => (");
lines.push("              <div key={i} style={{display:'flex',justifyContent:'space-between',padding:'6px 0',borderTop:'1px solid '+bd}}>");
lines.push("                <span style={{fontSize:'13px'}}>{c.name}</span>");
lines.push("                <span style={{fontSize:'13px',color:dg}}>{c.dose} {c.unit} · {c.frequency}</span>");
lines.push("              </div>");
lines.push("            ))}");
lines.push("          </div>");
lines.push("        ))}");
lines.push("      </div>");
lines.push("    </main>");
lines.push("  )");
lines.push("}");
fs.writeFileSync('app/protocol/page.tsx', lines.join('\n'), 'utf8');
console.log('Done');
