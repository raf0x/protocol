const fs = require('fs');
const lines = [];
lines.push("'use client'");
lines.push("");
lines.push("import { useState } from 'react'");
lines.push("import { createClient } from '../../lib/supabase'");
lines.push("import { useRouter } from 'next/navigation'");
lines.push("");
lines.push("const steps = [");
lines.push("  {");
lines.push("    title: 'Welcome to Protocol',");
lines.push("    body: 'Protocol is a private harm reduction tracker. Everything you log stays with you — your protocols, your journal, your data.',");
lines.push("    cta: 'Next',");
lines.push("  },");
lines.push("  {");
lines.push("    title: 'Track what you take',");
lines.push("    body: 'Build a protocol — add the compounds you use, your doses, and your schedule. The Pep Calculator helps you mix peptides safely.',");
lines.push("    cta: 'Next',");
lines.push("  },");
lines.push("  {");
lines.push("    title: 'Log how you feel',");
lines.push("    body: 'The daily journal tracks your mood, energy, and sleep. Over time you will see patterns — what is working, what is not.',");
lines.push("    cta: 'Next',");
lines.push("  },");
lines.push("  {");
lines.push("    title: 'Learn and connect',");
lines.push("    body: 'The Learn section has harm reduction guides. Community cohorts let you read experiences from others anonymously — no real names, no vendor talk.',");
lines.push("    cta: 'Next',");
lines.push("  },");
lines.push("  {");
lines.push("    title: 'One important thing',");
lines.push("    body: 'Protocol does not provide medical advice. It is a personal tracking tool. Always consult a qualified healthcare provider before starting any new compound.',");
lines.push("    cta: 'Get started',");
lines.push("  },");
lines.push("]");
lines.push("");
lines.push("export default function OnboardingPage() {");
lines.push("  const [step, setStep] = useState(0)");
lines.push("  const [loading, setLoading] = useState(false)");
lines.push("  const router = useRouter()");
lines.push("  const g = '#39ff14'");
lines.push("  const dg = '#4dbd4d'");
lines.push("  const mg = '#2d5a2d'");
lines.push("  const cb = '#0d0d0d'");
lines.push("  const bd = '#1a1a1a'");
lines.push("  const current = steps[step]");
lines.push("  const isLast = step === steps.length - 1");
lines.push("");
lines.push("  async function advance() {");
lines.push("    if (!isLast) { setStep(step + 1); return }");
lines.push("    setLoading(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (user) {");
lines.push("      await supabase.from('user_profiles').upsert({ id: user.id, onboarded: true })");
lines.push("    }");
lines.push("    router.push('/protocol')");
lines.push("  }");
lines.push("");
lines.push("  async function skip() {");
lines.push("    setLoading(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (user) {");
lines.push("      await supabase.from('user_profiles').upsert({ id: user.id, onboarded: true })");
lines.push("    }");
lines.push("    router.push('/protocol')");
lines.push("  }");
lines.push("");
lines.push("  return (");
lines.push("    <main style={{minHeight:'100vh',background:'#000000',color:'white',display:'flex',flexDirection:'column',alignItems:'center',justifyContent:'center',padding:'32px 24px'}}>");
lines.push("      <div style={{maxWidth:'400px',width:'100%'}}>");
lines.push("        <div style={{display:'flex',gap:'6px',marginBottom:'40px',justifyContent:'center'}}>");
lines.push("          {steps.map((_, i) => (");
lines.push("            <div key={i} style={{width: i === step ? '24px' : '6px',height:'6px',borderRadius:'3px',background:i<=step?g:bd,transition:'width 0.3s'}} />");
lines.push("          ))}");
lines.push("        </div>");
lines.push("        <h1 style={{fontSize:'28px',fontWeight:'bold',color:g,marginBottom:'16px',lineHeight:'1.2'}}>{current.title}</h1>");
lines.push("        <p style={{color:dg,fontSize:'15px',lineHeight:'1.7',marginBottom:'48px'}}>{current.body}</p>");
lines.push("        <button onClick={advance} disabled={loading} style={{width:'100%',background:g,color:'#000000',fontWeight:'700',padding:'16px',borderRadius:'8px',border:'none',fontSize:'16px',cursor:'pointer',marginBottom:'16px',letterSpacing:'1px'}}>");
lines.push("          {loading ? 'Loading...' : current.cta}");
lines.push("        </button>");
lines.push("        {!isLast && (");
lines.push("          <button onClick={skip} style={{width:'100%',background:'none',border:'none',color:mg,fontSize:'14px',cursor:'pointer'}}>Skip intro</button>");
lines.push("        )}");
lines.push("      </div>");
lines.push("    </main>");
lines.push("  )");
lines.push("}");
fs.writeFileSync('app/onboarding/page.tsx', lines.join('\n'), 'utf8');
console.log('Done');
