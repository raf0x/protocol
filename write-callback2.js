const fs = require('fs');
const lines = [];
lines.push("import { createServerClient } from '@supabase/ssr'");
lines.push("import { NextResponse } from 'next/server'");
lines.push("import type { NextRequest } from 'next/server'");
lines.push("");
lines.push("export async function GET(request: NextRequest) {");
lines.push("  const requestUrl = new URL(request.url)");
lines.push("  const code = requestUrl.searchParams.get('code')");
lines.push("  const token_hash = requestUrl.searchParams.get('token_hash')");
lines.push("  const type = requestUrl.searchParams.get('type')");
lines.push("");
lines.push("  const { cookies } = await import('next/headers')");
lines.push("  const cookieStore = await cookies()");
lines.push("  const supabase = createServerClient(");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_URL!,");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,");
lines.push("    {");
lines.push("      cookies: {");
lines.push("        getAll() { return cookieStore.getAll() },");
lines.push("        setAll(cookiesToSet) {");
lines.push("          cookiesToSet.forEach(({ name, value, options }) => cookieStore.set(name, value, options))");
lines.push("        },");
lines.push("      },");
lines.push("    }");
lines.push("  )");
lines.push("");
lines.push("  if (token_hash && type) {");
lines.push("    await supabase.auth.verifyOtp({ token_hash, type: type as any })");
lines.push("  } else if (code) {");
lines.push("    await supabase.auth.exchangeCodeForSession(code)");
lines.push("  }");
lines.push("");
lines.push("  const { data: { user } } = await supabase.auth.getUser()");
lines.push("  if (!user) return NextResponse.redirect(new URL('/auth/login', request.url))");
lines.push("");
lines.push("  const { data: profile } = await supabase");
lines.push("    .from('user_profiles')");
lines.push("    .select('onboarded')");
lines.push("    .eq('id', user.id)");
lines.push("    .single()");
lines.push("");
lines.push("  if (!profile || !profile.onboarded) {");
lines.push("    return NextResponse.redirect(new URL('/onboarding', request.url))");
lines.push("  }");
lines.push("");
lines.push("  return NextResponse.redirect(new URL('/protocol', request.url))");
lines.push("}");
fs.writeFileSync('app/auth/callback/route.ts', lines.join('\n'), 'utf8');
console.log('Done');
