const fs = require('fs');
const lines = [];
lines.push("'use client'");
lines.push("");
lines.push("import { useState, useEffect } from 'react'");
lines.push("import { createClient } from '../../lib/supabase'");
lines.push("");
lines.push("type JournalEntry = { id: string; date: string; mood: number | null; energy: number | null; sleep: number | null; notes: string; weight?: number; hunger?: number }");
lines.push("type DueCompound = { id: string; name: string; dose: string; protocol_name: string }");
lines.push("type LogEntry = { compound_id: string; taken: boolean; discomfort: number }");
lines.push("");
lines.push("function isDueToday(frequency: string, protocolStart: string, dayOfWeek: number | null): boolean {");
lines.push("  if (!protocolStart) return false");
lines.push("  const start = new Date(protocolStart + 'T00:00:00')");
lines.push("  const today = new Date(); today.setHours(0,0,0,0)");
lines.push("  const daysDiff = Math.floor((today.getTime() - start.getTime()) / 86400000)");
lines.push("  if (daysDiff < 0) return false");
lines.push("  if (frequency === 'daily') return true");
lines.push("  if (frequency === 'eod') return daysDiff % 2 === 0");
lines.push("  if (frequency === 'every3days') return daysDiff % 3 === 0");
lines.push("  if (frequency === 'every4days') return daysDiff % 4 === 0");
lines.push("  if (frequency === 'every5days') return daysDiff % 5 === 0");
lines.push("  const todayDay = today.getDay()");
lines.push("  if (frequency === '1x/week') return dayOfWeek !== null ? todayDay === dayOfWeek : daysDiff % 7 === 0");
lines.push("  if (frequency === '2x/week') { const d = dayOfWeek ?? 0; return todayDay === d || todayDay === (d+3)%7 }");
lines.push("  if (frequency === '3x/week') return todayDay === 1 || todayDay === 3 || todayDay === 5");
lines.push("  if (frequency === '4x/week') return todayDay === 1 || todayDay === 2 || todayDay === 4 || todayDay === 5");
lines.push("  if (frequency === '5x/week') return todayDay >= 1 && todayDay <= 5");
lines.push("  if (frequency === '6x/week') return todayDay !== 0");
lines.push("  return false");
lines.push("}");
lines.push("");
lines.push("export default function JournalPage() {");
lines.push("  const [entries, setEntries] = useState<JournalEntry[]>([])");
lines.push("  const [loading, setLoading] = useState(true)");
lines.push("  const [saving, setSaving] = useState(false)");
lines.push("  const [error, setError] = useState('')");
lines.push("");
lines.push("  const today = new Date().toISOString().split('T')[0]");
lines.push("  const [date, setDate] = useState(today)");
lines.push("  const [mood, setMood] = useState<number | null>(null)");
lines.push("  const [energy, setEnergy] = useState<number | null>(null)");
lines.push("  const [sleep, setSleep] = useState('')");
lines.push("  const [weight, setWeight] = useState('')");
lines.push("  const [hunger, setHunger] = useState<number | null>(null)");
lines.push("  const [entryNotes, setEntryNotes] = useState('')");
lines.push("");
lines.push("  const [dueCompounds, setDueCompounds] = useState<DueCompound[]>([])");
lines.push("  const [logs, setLogs] = useState<Record<string, LogEntry>>({})");
lines.push("");
lines.push("  const g = '#39ff14'");
lines.push("  const dg = '#8b8ba7'");
lines.push("  const mg = '#3d3d5c'");
lines.push("  const cb = '#12121a'");
lines.push("  const bd = '#1e1e2e'");
lines.push("");
lines.push("  useEffect(() => { loadAll() }, [])");
lines.push("");
lines.push("  async function loadAll() {");
lines.push("    setLoading(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setLoading(false); return }");
lines.push("    const { data: js } = await supabase.from('journal_entries').select('*').order('date', { ascending: false })");
lines.push("    setEntries(js || [])");
lines.push("    const todayEntry = (js || []).find((e: any) => e.date === today)");
lines.push("    if (todayEntry) {");
lines.push("      setMood(todayEntry.mood); setEnergy(todayEntry.energy);");
lines.push("      setSleep(todayEntry.sleep?.toString() || '')");
lines.push("      setWeight(todayEntry.weight?.toString() || '')");
lines.push("      setHunger(todayEntry.hunger ?? null)");
lines.push("      setEntryNotes(todayEntry.notes || '')");
lines.push("    }");
lines.push("    const { data: protocols } = await supabase.from('protocols').select('id, start_date, name, compounds(id, name, phases(dose, dose_unit, frequency, day_of_week, start_week, end_week))').eq('status', 'active')");
lines.push("    const due: DueCompound[] = []");
lines.push("    ;(protocols || []).forEach((p: any) => {");
lines.push("      const startMs = new Date(p.start_date + 'T00:00:00').getTime()");
lines.push("      const daysIn = Math.floor((Date.now() - startMs) / 86400000)");
lines.push("      const currentWeek = Math.max(1, Math.floor(daysIn / 7) + 1)");
lines.push("      ;(p.compounds || []).forEach((c: any) => {");
lines.push("        const phase = (c.phases || []).find((ph: any) => currentWeek >= ph.start_week && currentWeek <= ph.end_week) || c.phases?.[0]");
lines.push("        if (!phase) return");
lines.push("        if (isDueToday(phase.frequency, p.start_date, phase.day_of_week)) {");
lines.push("          due.push({ id: c.id, name: c.name, dose: phase.dose + phase.dose_unit, protocol_name: p.name })");
lines.push("        }");
lines.push("      })");
lines.push("    })");
lines.push("    setDueCompounds(due)");
lines.push("    const { data: ls } = await supabase.from('injection_logs').select('*').eq('date', today)");
lines.push("    const map: Record<string, LogEntry> = {}");
lines.push("    ;(ls || []).forEach((l: any) => { map[l.compound_id] = { compound_id: l.compound_id, taken: l.taken, discomfort: l.discomfort } })");
lines.push("    setLogs(map)");
lines.push("    setLoading(false)");
lines.push("  }");
lines.push("");
lines.push("  async function toggleInjection(compoundId: string) {");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) return");
lines.push("    const current = logs[compoundId]");
lines.push("    const newTaken = !current?.taken");
lines.push("    await supabase.from('injection_logs').upsert({ user_id: user.id, compound_id: compoundId, date: today, taken: newTaken, discomfort: current?.discomfort || 0 }, { onConflict: 'user_id,compound_id,date' })");
lines.push("    setLogs({ ...logs, [compoundId]: { compound_id: compoundId, taken: newTaken, discomfort: current?.discomfort || 0 } })");
lines.push("  }");
lines.push("");
lines.push("  async function setDiscomfort(compoundId: string, level: number) {");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) return");
lines.push("    await supabase.from('injection_logs').upsert({ user_id: user.id, compound_id: compoundId, date: today, taken: true, discomfort: level }, { onConflict: 'user_id,compound_id,date' })");
lines.push("    setLogs({ ...logs, [compoundId]: { compound_id: compoundId, taken: true, discomfort: level } })");
lines.push("  }");
lines.push("");
lines.push("  async function saveEntry() {");
lines.push("    setError(''); setSaving(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { setError('Not signed in.'); setSaving(false); return }");
lines.push("    const row: any = { user_id: user.id, date, notes: entryNotes.trim() }");
lines.push("    if (mood !== null) row.mood = mood");
lines.push("    if (energy !== null) row.energy = energy");
lines.push("    if (sleep) row.sleep = parseFloat(sleep)");
lines.push("    if (weight) row.weight = parseFloat(weight)");
lines.push("    if (hunger !== null) row.hunger = hunger");
lines.push("    const { error: e } = await supabase.from('journal_entries').upsert(row, { onConflict: 'user_id,date' })");
lines.push("    if (e) { setError(e.message); setSaving(false); return }");
lines.push("    setSaving(false)");
lines.push("    loadAll()");
lines.push("  }");
lines.push("");
lines.push("  function ScoreBtn({ value, current, onChange, activeColor = g }: { value: number; current: number | null; onChange: (v: number) => void; activeColor?: string }) {");
lines.push("    const isActive = current === value");
lines.push("    return <button onClick={() => onChange(value)} style={{width:'42px',height:'42px',borderRadius:'50%',border:isActive?'none':'1px solid '+bd,background:isActive?activeColor:cb,color:isActive?'#000':dg,fontSize:'14px',fontWeight:'700',cursor:'pointer'}}>{value}</button>");
lines.push("  }");
lines.push("");
lines.push("  function DiscomfortBtn({ value, current, onChange }: { value: number; current: number; onChange: (v: number) => void }) {");
lines.push("    const isActive = current === value");
lines.push("    const color = value === 0 ? g : '#ff6b6b'");
lines.push("    return <button onClick={() => onChange(value)} style={{width:'32px',height:'32px',borderRadius:'6px',border:'1px solid '+(isActive?color:bd),background:isActive?(value===0?'rgba(57,255,20,0.15)':'rgba(255,107,107,0.15)'):'transparent',color:isActive?color:dg,fontSize:'12px',fontWeight:'700',cursor:'pointer'}}>{value}</button>");
lines.push("  }");
lines.push("");
lines.push("  const inputStyle = { width:'100%', background:'#0a0a0f', border:'1px solid '+bd, borderRadius:'6px', padding:'8px 10px', color:'white', fontSize:'14px', boxSizing:'border-box' as const, colorScheme:'dark' as const }");
lines.push("");
lines.push("  if (loading) return <main style={{minHeight:'100vh',color:dg,display:'flex',alignItems:'center',justifyContent:'center'}}>Loading...</main>");
lines.push("");
lines.push("  return (");
lines.push("    <main style={{minHeight:'100vh',color:'white',padding:'24px'}}>");
lines.push("      <div style={{maxWidth:'540px',margin:'0 auto'}}>");
lines.push("        <h1 style={{fontSize:'24px',fontWeight:'bold',color:g,marginBottom:'8px'}}>Journal</h1>");
lines.push("        <p style={{color:dg,fontSize:'13px',marginBottom:'20px'}}>Log your day. Track your progress.</p>");
lines.push("");
lines.push("        {/* Today's injections */}");
lines.push("        {dueCompounds.length > 0 && (");
lines.push("          <div style={{background:cb,border:'1px solid '+bd,borderRadius:'12px',padding:'16px',marginBottom:'16px'}}>");
lines.push("            <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'12px'}}>");
lines.push("              <span style={{fontSize:'13px',fontWeight:'700',color:'#ffffff',letterSpacing:'1px'}}>TODAY'S INJECTIONS</span>");
lines.push("              <span style={{fontSize:'12px',color:mg}}>{Object.values(logs).filter(l => l.taken).length}/{dueCompounds.length}</span>");
lines.push("            </div>");
lines.push("            {dueCompounds.map(c => {");
lines.push("              const log = logs[c.id]");
lines.push("              const taken = log?.taken || false");
lines.push("              const discomfort = log?.discomfort || 0");
lines.push("              return (");
lines.push("                <div key={c.id} style={{background:'#0a0a0f',border:'1px solid '+bd,borderRadius:'8px',padding:'12px',marginBottom:'8px'}}>");
lines.push("                  <div style={{display:'flex',alignItems:'center',gap:'12px'}}>");
lines.push("                    <button onClick={() => toggleInjection(c.id)} style={{width:'26px',height:'26px',borderRadius:'6px',border:'1px solid '+(taken?g:bd),background:taken?g:'transparent',cursor:'pointer',color:'#000',fontWeight:'800',padding:0}}>{taken?'✓':''}</button>");
lines.push("                    <div style={{flex:1}}>");
lines.push("                      <div style={{fontSize:'14px',fontWeight:'600',color:taken?dg:'white',textDecoration:taken?'line-through':'none'}}>{c.name}</div>");
lines.push("                      <div style={{fontSize:'11px',color:mg}}>{c.dose} · {c.protocol_name}</div>");
lines.push("                    </div>");
lines.push("                  </div>");
lines.push("                  {taken && (");
lines.push("                    <div style={{marginTop:'10px',paddingTop:'10px',borderTop:'1px solid '+bd}}>");
lines.push("                      <span style={{fontSize:'10px',color:mg,display:'block',marginBottom:'6px',letterSpacing:'1px'}}>DISCOMFORT (0 = none)</span>");
lines.push("                      <div style={{display:'flex',gap:'6px'}}>");
lines.push("                        {[0,1,2,3,4,5].map(n => <DiscomfortBtn key={n} value={n} current={discomfort} onChange={v => setDiscomfort(c.id, v)} />)}");
lines.push("                      </div>");
lines.push("                    </div>");
lines.push("                  )}");
lines.push("                </div>");
lines.push("              )");
lines.push("            })}");
lines.push("          </div>");
lines.push("        )}");
lines.push("");
lines.push("        {/* Daily log */}");
lines.push("        <div style={{background:cb,border:'1px solid '+bd,borderRadius:'12px',padding:'20px',marginBottom:'16px'}}>");
lines.push("          <h2 style={{fontSize:'13px',fontWeight:'700',color:'#ffffff',letterSpacing:'1px',marginBottom:'16px'}}>HOW ARE YOU TODAY?</h2>");
lines.push("");
lines.push("          <div style={{marginBottom:'16px'}}>");
lines.push("            <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'6px'}}>Date</label>");
lines.push("            <input type='date' value={date} onChange={e => setDate(e.target.value)} style={inputStyle} />");
lines.push("          </div>");
lines.push("");
lines.push("          <div style={{marginBottom:'16px'}}>");
lines.push("            <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'8px'}}>Mood</label>");
lines.push("            <div style={{display:'flex',gap:'8px'}}>{[1,2,3,4,5].map(v => <ScoreBtn key={v} value={v} current={mood} onChange={setMood} />)}</div>");
lines.push("          </div>");
lines.push("");
lines.push("          <div style={{marginBottom:'16px'}}>");
lines.push("            <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'8px'}}>Energy</label>");
lines.push("            <div style={{display:'flex',gap:'8px'}}>{[1,2,3,4,5].map(v => <ScoreBtn key={v} value={v} current={energy} onChange={setEnergy} activeColor='#f59e0b' />)}</div>");
lines.push("          </div>");
lines.push("");
lines.push("          <div style={{marginBottom:'16px'}}>");
lines.push("            <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'8px'}}>Hunger (1 = suppressed, 5 = ravenous)</label>");
lines.push("            <div style={{display:'flex',gap:'8px'}}>{[1,2,3,4,5].map(v => <ScoreBtn key={v} value={v} current={hunger} onChange={setHunger} activeColor='#8b5cf6' />)}</div>");
lines.push("          </div>");
lines.push("");
lines.push("          <div style={{display:'grid',gridTemplateColumns:'1fr 1fr',gap:'10px',marginBottom:'16px'}}>");
lines.push("            <div>");
lines.push("              <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'6px'}}>Sleep (hrs)</label>");
lines.push("              <input type='number' step='0.5' value={sleep} onChange={e => setSleep(e.target.value)} placeholder='e.g. 7.5' style={inputStyle} />");
lines.push("            </div>");
lines.push("            <div>");
lines.push("              <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'6px'}}>Weight (lbs)</label>");
lines.push("              <input type='number' step='0.1' value={weight} onChange={e => setWeight(e.target.value)} placeholder='optional' style={inputStyle} />");
lines.push("            </div>");
lines.push("          </div>");
lines.push("");
lines.push("          <div style={{marginBottom:'16px'}}>");
lines.push("            <label style={{display:'block',fontSize:'12px',color:dg,marginBottom:'6px'}}>Notes</label>");
lines.push("            <textarea value={entryNotes} onChange={e => setEntryNotes(e.target.value)} placeholder='How do you feel? Any observations...' rows={3} style={{...inputStyle,resize:'none'}} />");
lines.push("          </div>");
lines.push("");
lines.push("          {error && <div style={{background:'#1a0000',border:'1px solid #4a0000',borderRadius:'6px',padding:'10px',fontSize:'13px',color:'#ff6b6b',marginBottom:'12px'}}>{error}</div>}");
lines.push("");
lines.push("          <button onClick={saveEntry} disabled={saving} style={{width:'100%',background:saving?'#1a3d1a':g,color:saving?mg:'#000',border:'none',borderRadius:'6px',padding:'12px',fontSize:'14px',fontWeight:'700',cursor:saving?'not-allowed':'pointer'}}>{saving?'Saving...':'Save Entry'}</button>");
lines.push("        </div>");
lines.push("");
lines.push("        {/* Recent entries */}");
lines.push("        <h2 style={{fontSize:'13px',fontWeight:'700',color:'#ffffff',letterSpacing:'1px',marginTop:'24px',marginBottom:'12px'}}>RECENT ENTRIES</h2>");
lines.push("        {entries.length === 0 && <p style={{color:mg,fontSize:'13px'}}>No entries yet.</p>}");
lines.push("        {entries.slice(0, 7).map(e => (");
lines.push("          <div key={e.id} style={{background:cb,border:'1px solid '+bd,borderRadius:'8px',padding:'12px',marginBottom:'8px'}}>");
lines.push("            <div style={{display:'flex',justifyContent:'space-between',marginBottom:'6px'}}>");
lines.push("              <span style={{fontSize:'13px',fontWeight:'600',color:g}}>{new Date(e.date + 'T12:00:00').toLocaleDateString('en-US',{month:'short',day:'numeric'})}</span>");
lines.push("            </div>");
lines.push("            <div style={{display:'flex',gap:'12px',fontSize:'12px',color:dg,flexWrap:'wrap'}}>");
lines.push("              {e.mood !== null && <span>Mood {e.mood}</span>}");
lines.push("              {e.energy !== null && <span>· Energy {e.energy}</span>}");
lines.push("              {e.sleep !== null && <span>· Sleep {e.sleep}h</span>}");
lines.push("              {(e as any).weight && <span>· {(e as any).weight}lbs</span>}");
lines.push("              {(e as any).hunger !== null && (e as any).hunger !== undefined && <span>· Hunger {(e as any).hunger}</span>}");
lines.push("            </div>");
lines.push("            {e.notes && <p style={{fontSize:'12px',color:dg,marginTop:'6px'}}>{e.notes}</p>}");
lines.push("          </div>");
lines.push("        ))}");
lines.push("      </div>");
lines.push("    </main>");
lines.push("  )");
lines.push("}");
fs.writeFileSync('app/journal/page.tsx', lines.join('\n'), 'utf8');
console.log('Done');
