const fs = require('fs');
const lines = [];
lines.push("import { createServerClient } from '@supabase/ssr'");
lines.push("import { NextResponse } from 'next/server'");
lines.push("import type { NextRequest } from 'next/server'");
lines.push("");
lines.push("export async function middleware(request: NextRequest) {");
lines.push("  const response = NextResponse.next()");
lines.push("  const supabase = createServerClient(");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_URL!,");
lines.push("    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,");
lines.push("    {");
lines.push("      cookies: {");
lines.push("        getAll() { return request.cookies.getAll() },");
lines.push("        setAll(cookiesToSet) {");
lines.push("          cookiesToSet.forEach(({ name, value, options }) => {");
lines.push("            request.cookies.set(name, value)");
lines.push("            response.cookies.set(name, value, options)");
lines.push("          })");
lines.push("        },");
lines.push("      },");
lines.push("    }");
lines.push("  )");
lines.push("  const { data: { user } } = await supabase.auth.getUser()");
lines.push("  const isAuthPage = request.nextUrl.pathname.startsWith('/auth')");
lines.push("  const isPublicPage = request.nextUrl.pathname === '/' || request.nextUrl.pathname === '/calculator' || request.nextUrl.pathname === '/onboarding'");
lines.push("  if (!user && !isAuthPage && !isPublicPage) {");
lines.push("    return NextResponse.redirect(new URL('/auth/login', request.url))");
lines.push("  }");
lines.push("  return response");
lines.push("}");
lines.push("");
lines.push("export const config = {");
lines.push("  matcher: ['/((?!_next/static|_next/image|favicon.ico|.*\\.png$).*)'],");
lines.push("}");
fs.writeFileSync('middleware.ts', lines.join('\n'), 'utf8');
console.log('Done');
