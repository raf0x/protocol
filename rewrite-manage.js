"                        </button>",
"                      ))}",
"                    </div>",
"                  </div>",
"                </div>",
"",
"                {/* Inventory */}",
"                <div style={{marginBottom:'12px'}}>",
"                  <label style={{display:'block',fontSize:'11px',color:dg,fontWeight:'700',letterSpacing:'1px',marginBottom:'6px'}}>VIALS IN STOCK</label>",
