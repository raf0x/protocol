"        vial_unit: c.vial_unit,",
"        bac_water_ml: c.bac_water_ml ? parseFloat(c.bac_water_ml) : null,",
"        reconstitution_date: c.reconstitution_date,",
"        notes: c.notes.trim(),",
"        vials_in_stock: c.vials_in_stock ? parseInt(c.vials_in_stock) : null,",
"        position: ci",
"      }).select().single()",
"      if (!ins) continue",
