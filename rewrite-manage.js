"                    </select>",
"                  </div>",
"                </div>",
"",
"                {/* Duration */}",
"                <div style={{marginBottom:'16px'}}>",
"                  <label style={{display:'block',fontSize:'11px',color:dg,fontWeight:'700',letterSpacing:'1px',marginBottom:'6px'}}>PROTOCOL DURATION</label>",
"                  <div style={{display:'flex',gap:'8px',alignItems:'center'}}>",
