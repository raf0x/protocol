const fs = require('fs');
const lines = [];
lines.push("'use client'");
lines.push("");
lines.push("import { useState, useEffect } from 'react'");
lines.push("import { createClient } from '../../lib/supabase'");
lines.push("import { useRouter } from 'next/navigation'");
lines.push("");
lines.push("type Cohort = { id: string; name: string; description: string; parent_id: string | null }");
lines.push("");
lines.push("export default function CommunityPage() {");
lines.push("  const [username, setUsername] = useState<string | null>(null)");
lines.push("  const [newUsername, setNewUsername] = useState('')");
lines.push("  const [cohorts, setCohorts] = useState<Cohort[]>([])");
lines.push("  const [joined, setJoined] = useState<string[]>([])");
lines.push("  const [loading, setLoading] = useState(true)");
lines.push("  const [saving, setSaving] = useState(false)");
lines.push("  const [error, setError] = useState('')");
lines.push("  const router = useRouter()");
lines.push("  const g = '#39ff14'");
lines.push("  const dg = '#4dbd4d'");
lines.push("  const mg = '#2d5a2d'");
lines.push("  const cb = '#0d0d0d'");
lines.push("  const bd = '#1a1a1a'");
lines.push("");
lines.push("  useEffect(() => { loadData() }, [])");
lines.push("");
lines.push("  async function loadData() {");
lines.push("    setLoading(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) { router.push('/auth/login'); return }");
lines.push("    const { data: un } = await supabase.from('usernames').select('username').eq('user_id', user.id).single()");
lines.push("    if (un) setUsername(un.username)");
lines.push("    const { data: ch } = await supabase.from('cohorts').select('*').order('name')");
lines.push("    setCohorts(ch || [])");
lines.push("    const { data: mem } = await supabase.from('cohort_members').select('cohort_id').eq('user_id', user.id)");
lines.push("    setJoined((mem || []).map((m: any) => m.cohort_id))");
lines.push("    setLoading(false)");
lines.push("  }");
lines.push("");
lines.push("  async function saveUsername() {");
lines.push("    setError('')");
lines.push("    const clean = newUsername.trim().replace(/[^a-zA-Z0-9_]/g, '')");
lines.push("    if (clean.length < 3) { setError('Username must be at least 3 characters. Letters, numbers, and underscores only.'); return }");
lines.push("    setSaving(true)");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) return");
lines.push("    const { error: e } = await supabase.from('usernames').insert({ user_id: user.id, username: clean })");
lines.push("    if (e) { setError(e.message.includes('unique') ? 'That username is taken. Try another.' : e.message); setSaving(false); return }");
lines.push("    setUsername(clean)");
lines.push("    setSaving(false)");
lines.push("  }");
lines.push("");
lines.push("  async function toggleJoin(cohortId: string) {");
lines.push("    const supabase = createClient()");
lines.push("    const { data: { user } } = await supabase.auth.getUser()");
lines.push("    if (!user) return");
lines.push("    if (joined.includes(cohortId)) {");
lines.push("      await supabase.from('cohort_members').delete().eq('user_id', user.id).eq('cohort_id', cohortId)");
lines.push("      setJoined(joined.filter(id => id !== cohortId))");
lines.push("    } else {");
lines.push("      await supabase.from('cohort_members').insert({ user_id: user.id, cohort_id: cohortId })");
lines.push("      setJoined([...joined, cohortId])");
lines.push("    }");
lines.push("  }");
lines.push("");
lines.push("  const parents = cohorts.filter(c => !c.parent_id)");
lines.push("  const subCohorts = (parentId: string) => cohorts.filter(c => c.parent_id === parentId)");
lines.push("");
lines.push("  if (loading) return <main style={{minHeight:'100vh',background:'#000000',color:dg,display:'flex',alignItems:'center',justifyContent:'center'}}>Loading...</main>");
lines.push("");
lines.push("  if (!username) {");
lines.push("    return (");
lines.push("      <main style={{minHeight:'100vh',background:'#000000',color:'white',display:'flex',flexDirection:'column',alignItems:'center',justifyContent:'center',padding:'24px'}}>"); 
lines.push("        <div style={{maxWidth:'400px',width:'100%'}}>"); 
lines.push("          <h1 style={{fontSize:'24px',fontWeight:'bold',marginBottom:'8px',color:g}}>Choose a username</h1>"); 
lines.push("          <p style={{color:dg,fontSize:'13px',marginBottom:'24px'}}>Your username is how you appear in cohorts. It cannot be changed later. No personal information is visible to other users.</p>"); 
lines.push("          <input value={newUsername} onChange={e => setNewUsername(e.target.value)} placeholder='e.g. peptide_tracker' style={{width:'100%',background:cb,border:'1px solid '+bd,borderRadius:'6px',padding:'12px',color:'white',fontSize:'16px',outline:'none',boxSizing:'border-box',marginBottom:'12px'}} />"); 
lines.push("          {error && <div style={{background:'#1a0000',border:'1px solid #4a0000',borderRadius:'6px',padding:'10px',fontSize:'13px',color:'#ff6b6b',marginBottom:'12px'}}>{error}</div>}"); 
lines.push("          <button onClick={saveUsername} disabled={saving} style={{width:'100%',background:saving?'#1a3d1a':g,color:saving?mg:'#000000',fontWeight:'700',padding:'14px',borderRadius:'6px',border:'none',fontSize:'16px',cursor:saving?'not-allowed':'pointer'}}>{saving?'Saving...':'Set username'}</button>"); 
lines.push("          <p style={{color:mg,fontSize:'11px',marginTop:'16px',textAlign:'center'}}>Letters, numbers, and underscores only. Minimum 3 characters.</p>"); 
lines.push("        </div>"); 
lines.push("      </main>"); 
lines.push("    )"); 
lines.push("  }"); 
lines.push("");
lines.push("  return (");
lines.push("    <main style={{minHeight:'100vh',background:'#000000',color:'white',padding:'24px'}}>"); 
lines.push("      <div style={{maxWidth:'480px',margin:'0 auto'}}>"); 
lines.push("        <div style={{display:'flex',justifyContent:'space-between',alignItems:'center',marginBottom:'8px'}}>"); 
lines.push("          <h1 style={{fontSize:'24px',fontWeight:'bold',color:g}}>Community</h1>"); 
lines.push("          <span style={{fontSize:'12px',color:mg}}>@{username}</span>"); 
lines.push("        </div>"); 
lines.push("        <p style={{color:dg,fontSize:'13px',marginBottom:'24px'}}>Join cohorts to read experiences from others. No vendor discussion allowed.</p>"); 
lines.push("        {parents.map(parent => ("); 
lines.push("          <div key={parent.id} style={{marginBottom:'24px'}}>"); 
lines.push("            <h2 style={{fontSize:'16px',fontWeight:'700',color:g,marginBottom:'12px'}}>{parent.name}</h2>"); 
lines.push("            {subCohorts(parent.id).map(sub => {"); 
lines.push("              const isJoined = joined.includes(sub.id)"); 
lines.push("              return ("); 
lines.push("                <div key={sub.id} style={{background:cb,border:'1px solid '+(isJoined?mg:bd),borderRadius:'8px',padding:'14px',marginBottom:'8px',display:'flex',justifyContent:'space-between',alignItems:'center'}}>"); 
lines.push("                  <div style={{flex:1}}>"); 
lines.push("                    <div style={{display:'flex',alignItems:'center',gap:'8px',marginBottom:'4px'}}>"); 
lines.push("                      <span style={{fontSize:'14px',fontWeight:'600',color:isJoined?g:'white'}}>{sub.name}</span>"); 
lines.push("                      {isJoined && <span style={{fontSize:'10px',color:mg,background:'#0a1a0a',padding:'2px 6px',borderRadius:'4px'}}>JOINED</span>}"); 
lines.push("                    </div>"); 
lines.push("                    <p style={{fontSize:'12px',color:mg,margin:0}}>{sub.description}</p>"); 
lines.push("                  </div>"); 
lines.push("                  <div style={{display:'flex',gap:'8px',marginLeft:'12px'}}>"); 
lines.push("                    {isJoined && ("); 
lines.push("                      <button onClick={() => router.push('/community/cohorts/'+sub.id)} style={{background:'#0a1a0a',border:'1px solid '+mg,color:dg,fontSize:'12px',padding:'6px 10px',borderRadius:'6px',cursor:'pointer',border:'none'}}>View</button>"); 
lines.push("                    )}"); 
lines.push("                    <button onClick={() => toggleJoin(sub.id)} style={{background:isJoined?'#1a0000':'#0a1a0a',border:'1px solid '+(isJoined?'#4a0000':mg),color:isJoined?'#ff6b6b':dg,fontSize:'12px',padding:'6px 10px',borderRadius:'6px',cursor:'pointer'}}>{isJoined?'Leave':'Join'}</button>"); 
lines.push("                  </div>"); 
lines.push("                </div>"); 
lines.push("              )"); 
lines.push("            })}"); 
lines.push("          </div>"); 
lines.push("        ))}"); 
lines.push("      </div>"); 
lines.push("    </main>"); 
lines.push("  )"); 
lines.push("}");
fs.writeFileSync('app/community/page.tsx', lines.join('\n'), 'utf8');
console.log('Done');
